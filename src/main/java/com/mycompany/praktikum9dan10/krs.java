    private void nimKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nimKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == 10){
            try {
                koneksi();
                String sql = "SELECT * FROM mahasiswa WHERE nim = '" + nim.getText() + "'";
                ResultSet rs = cn.executeQuery(sql);
                
                if(rs.next()){
                    nama.setText(rs.getString(2));
                    kode_mtk.requestFocus();
                }else {
                    JOptionPane.showMessageDialog(null, "Data Mahasiswa Tidak Ada");
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ada Kesalahan Menampilkan Data Mahasiswa" + e);
            }
        }
    }//GEN-LAST:event_nimKeyPressed

