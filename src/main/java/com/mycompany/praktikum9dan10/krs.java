    void cekkrs(){
        DefaultTableModel tabelnyo = new DefaultTableModel();
        tabelnyo.addColumn("Kode");
        tabelnyo.addColumn("Matakuliah");
        tabelnyo.addColumn("SKS");
        
        try {
            koneksi();
            String sql = "SELECT krs.id, krs.krs_sem, krs.krs_id_mata_kuliah, " +
                    "krs.krs_no_bp, mahasiswa.nama, krs.krs_nilai, krs.krs_nilai, krs.krs_nilai " + 
                    "FROM krs JOIN mahasiswa ON krs.krs_no_bp = mahasiswa.nim " +
                    "JOIN mata_kuliah ON krs.krs_id_mata_kuliah = mata_kuliah.kode_mata_kuliah " +
                    "WHERE krs.krs_sem = '" + semester.getText() + "' AND krs.krs_no_bp = '" + nim.getText() + "'";
            
            ResultSet rs = cn.executeQuery(sql);
            while(rs.next()){
                tabelnyo.addRow(new Object[]{
                    rs.getString(3),
                    rs.getString(7),
                    rs.getString(8)
                });
            }
            jTable2.setModel(tabelnyo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan Menampilkan Data" + e);
        }
    }
    
